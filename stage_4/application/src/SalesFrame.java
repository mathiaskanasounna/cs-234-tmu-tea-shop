     public void addRowToJTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) showSalesTable.getModel();
            ArrayList<Sale> list = getSalesFromFile("src/data/sale.csv");
            Object rowData[] = new Object[3];
            for (int i = 0; i < list.size(); i++) {
                //firstName,lastName,email,streetAddress,city,zipCode,state,telephone,sales_id,clientId
                rowData[0] = list.get(i).getOrderId();
                rowData[1] = list.get(i).getOrderDate();
                rowData[2] = list.get(i).getProducts().get(0).getProductId();
                model.addRow(rowData);
            }
            //((DefaultTableModel) clientReportTable.getModel()).setRowCount(0);
            this.showSalesTable.setModel(model);
            showSalejScrollPane.setViewportView(showSalesTable);
        } catch (IOException ex) {
            Logger.getLogger(ClientFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    private Sale findSalesDetails(ArrayList<Sale> sales, String order_id) {
        for (Sale sale : sales) {
            if (sale.getOrderId().equals(order_id)) {
                System.out.println("Find the sale");
                return sale;
            }
        }
        System.out.println("Did not find the sale");
        return null;
    }

    private ArrayList<Sale> getSalesFromFile(String srcdatasalecsv) throws FileNotFoundException, IOException {
        ArrayList<Sale> sales = new ArrayList<>();
        // File path is passed as parameter
        File file = new File(srcdatasalecsv);
        BufferedReader br = new BufferedReader(new FileReader(file));
        // Declaring a string variable
        String st;
        // Condition holds true till
        // there is character in a string
        br.readLine();
        while ((st = br.readLine()) != null) // Print the string
        {
            //firstName,lastName,email,streetAddress,city,zipCode,state,telephone,sales_id,clientId
            String data[] = st.split(",");
            Product product = new Product();
            product.setProductId(data[2]);
            ArrayList<Product> products = new ArrayList<>();
            products.add(product);
            System.out.println("ArraySize " + data.length);
            Sale sale = new Sale(data[0], LocalDateTime.parse(data[1]), products);
            sales.add(sale);
        }
        return sales;
    }

    private String detailsOfSale(Sale sale) {
        if (sale != null) {
            //orderId,orderDate,ProductId
            String result = "Here are detail of the sale : \n";
            result = result + "Order ID : " + sale.getOrderId() + "\n Order Date : " + sale.getOrderDate()
                    + "\n Product Id " + sale.getProducts().get(0).getProductId();
            return result;
        } else {
            return "The Sale does not exist";
        }
    }

    private void saveSales(Sale sale, boolean mode) {
        System.out.println("Saving new sale");
        try {
            BufferedWriter writer = new BufferedWriter(new FileWriter("src/data/sale.csv", mode));
            if (mode == false) {
                writer.append("orderId,orderDate,ProductId");
            }
            writer.append("\n");
            writer.append(sale.getOrderId() + ",");
            writer.append(sale.getOrderDate() + ",");
            writer.append(sale.getProducts().get(0).getProductId());

            writer.close();

        } catch (IOException ex) {
            Logger.getLogger(SalesFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private ArrayList<Product> getProductFromFile(String srcdataclientcsv) throws FileNotFoundException, IOException {
        ArrayList<Product> clients = new ArrayList<>();
        // File path is passed as parameter
        File file = new File(srcdataclientcsv);
        BufferedReader br = new BufferedReader(new FileReader(file));
        // Declaring a string variable
        String st;
        // Condition holds true till
        // there is character in a string
        br.readLine();
        while ((st = br.readLine()) != null) // Print the string
        {
            //firstName,lastName,email,streetAddress,city,zipCode,state,telephone,sales_id,clientId
            String data[] = st.split(",");
            System.out.println("ArraySize " + data.length);
            Product product = new Product(data[0], data[1], data[2], Double.parseDouble(data[3]), Integer.parseInt(data[4]), null);
            clients.add(product);
        }
        return clients;
    }


