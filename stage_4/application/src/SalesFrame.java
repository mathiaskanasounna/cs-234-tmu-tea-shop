    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            // TODO add your handling code here
            String productId = productSalesTxt.getText();

            int productQuantity = Integer.parseInt(itemNumberSalesTxt.getText());

            Sale sale = new Sale();

            sale.setOrderDate(LocalDateTime.now());
            sale.setOrderId(orderIdSalesTxt.getText());

            TeaShop teaShop = new TeaShop();
            teaShop.setProducts(getProductFromFile("src/data/product.csv"));
            Product inventoryProduct = teaShop.getProduct(productId);

            if (inventoryProduct == null) {
                JOptionPane.showMessageDialog(this, "The Product Id does not exist");
            } else {
                Product product = new Product();
                product.setDistributor(inventoryProduct.getDistributor());
                product.setQuantity(productQuantity);
                product.setProductName(inventoryProduct.getProductName());
                product.setUnitPrice(inventoryProduct.getUnitPrice());
                product.setProductId(productId);
                inventoryProduct.setQuantity(inventoryProduct.getQuantity() - productQuantity);
                sale.addProduct(product);
                saveSales(sale, true);
                totalSalesTxt.setText(String.valueOf(sale.totalPrice()));
            }
        } catch (IOException ex) {
            Logger.getLogger(SalesFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_saveBtnActionPerformed

